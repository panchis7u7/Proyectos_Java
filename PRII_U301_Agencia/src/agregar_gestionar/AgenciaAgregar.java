    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        AgenciaDTO agencia = new AgenciaDTO();
        agencia.setNombre(tfNombre.getText());
        agencia.setCiudad(tfCiudad.getText());
        agencia.setTelefono(tfTelefono.getText());
        
        Conector conec = new Conector();
      
        AgenciaDAO AgenciaDAO = new AgenciaDAO(conec.miconector);
        int id = AgenciaDAO.agregarAgencia(agencia);
        if(id>0){
            JOptionPane.showMessageDialog(null, "Inserción exitosa");
        }else
            JOptionPane.showMessageDialog(null, "Inserción fallida");
        conec.cerrar();
    }//GEN-LAST:event_btnAgregarActionPerformed


